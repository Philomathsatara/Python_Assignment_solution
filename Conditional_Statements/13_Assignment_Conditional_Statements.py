# Ask user to enter number check if number is 0 or less than 0 or greater than 0

n = int(input("Enter number: ")) # take input from user

if (n==0): # condition to check number is zero

    print("Number is zero") # print 

elif (n<0): # condition to check number is less than zero

    print("Number is less than zero") # print 

else: 
    print("Number is greater than zero") # print


