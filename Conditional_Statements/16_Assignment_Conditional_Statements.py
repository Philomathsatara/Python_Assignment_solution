# Get number from user and check if number is odd or even, multiply number by 2 if number is even
# calculate square of the number if number is odd

n = int(input("Enter number: ")) # take input from user

if (n % 2 == 0): # condition to check number is even

    print("Number is even" ) # print 
    print("Number multiply by 2: ", n*2) # print

else: 

    print("Number is odd") # print
    print ("square of the number: ", n*n ) # print


