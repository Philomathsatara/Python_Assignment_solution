# Ask user to enter base and height of a triangle and calulate area of the triangle and print the same

b = int(input("Enter base of triangle:")) # Take base of triangle from user

h = int(input("Enter height of triangle:")) # Take height of triangle from user

area_of_the_triangle = (b*h)/2 # formula to calculate area of triangle

print("Area of Triangle is:", area_of_the_triangle) # print the result