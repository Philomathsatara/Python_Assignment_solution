# Ask user to enter radius and length of a cylinder and calculate volume of the cylinder and print the same.

r = float(input("Enter radius of cylinder:")) # Take radius of cylinder from user

h = float(input("Enter length of cylinder:")) # Take length of cylinder from user

volume_of_the_cylinder = 3.14*r**2*h # formula to calculate volume of the cylinder

print("volume of the cylinder is:", volume_of_the_cylinder) # print the result